package day001_day050.day042;

/**
 * 144. Binary Tree Preorder Traversal
 *
 * @author created by sunjy on 2/11/24
 */
public class TreeNode {

    int val;
    TreeNode left;
    TreeNode right;

    TreeNode() {
    }

    TreeNode(int val) {
        this.val = val;
    }

    TreeNode(int val, TreeNode left, TreeNode right) {
        this.val = val;
        this.left = left;
        this.right = right;
    }

}
