package day001_day050.day037;

/**
 * 24. Swap Nodes in Pairs
 *
 * @author created by sunjy on 2/6/24
 */
public class ListNode {

    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

}
